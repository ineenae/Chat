import Foundation

class Contact {
    
    var _name  = ""
    
    var _id = ""
    
    init(name : String , id : String) {
        _id = id
        _name = name
    }
    var name:String {
        get{
        return _name
        }
    }
    var id:String {
        return _id
    }
}





























































